// Icon color classes for consistent theming
export const ICON_COLORS = {
  PRIMARY: 'text-primary',
  SECONDARY: 'text-secondary',
  SUCCESS: 'text-success',
  DANGER: 'text-danger',
  WARNING: 'text-warning',
  INFO: 'text-info',
  LIGHT: 'text-light',
  DARK: 'text-dark',
  MUTED: 'text-muted',
  WHITE: 'text-white'
} as const;

// Helper function to get icon class with size and color
export const getIconClass = (
  icon: keyof typeof ICONS,
  size?: keyof typeof ICON_SIZES,
  color?: keyof typeof ICON_COLORS
): string => {
  let iconClass = ICONS[icon];
  
  if (size) {
    iconClass += ` ${ICON_SIZES[size]}`;
  }
  
  if (color) {
    iconClass += ` ${ICON_COLORS[color]}`;
  }
  
  return iconClass;
};

// Helper function to create icon element
export const createIcon = (
  icon: keyof typeof ICONS,
  size?: keyof typeof ICON_SIZES,
  color?: keyof typeof ICON_COLORS,
  className?: string
): string => {
  const iconClass = getIconClass(icon, size, color);
  return `<i class="${iconClass} ${className || ''}"></i>`;
};

